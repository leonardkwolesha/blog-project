import mongoose from "mongoose";

const ContactSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

// Prevent model overwrite errors in dev (hot reloads)
const Contact = mongoose.models.Contact || mongoose.model("Contact", ContactSchema);

export default Contact;
